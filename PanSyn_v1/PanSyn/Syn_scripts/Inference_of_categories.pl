#!/usr/bin/perl -w
use Getopt::Long;
my %opts;
GetOptions(\%opts,"g1=s","g2=s","a=s","s=s","o=s","h|help");
if (!( defined $opts{g1} and defined $opts{g2} and defined $opts{a} and defined $opts{s} and defined $opts{o})) {
		die "************************************************\n
	-g1	Full path to the [node1-OG.genes] file (generated by Ortholog_inference.pl)
	-g2	Full path to the [node2-OG.genes] file (generated by Ortholog_inference.pl)
	-a	Full path to the [All_species.emapper.annotations] file (generated by emapper.py)
	-s	Full path to the [syn_scripts] file PanSyn provided
	-o	Full path to the folder storing the output results 
	-h|-help	Print this help page
		*************************************************\n";
}
if (defined $opts{h} or defined $opts{help}) {
		die "************************************************\n
	-g1	Full path to the [node1-OG.genes] file (generated by Ortholog_inference.pl)
	-g2	Full path to the [node2-OG.genes] file (generated by Ortholog_inference.pl)
	-a	Full path to the [All_species.emapper.annotations] file (generated by emapper.py)
	-s	Full path to the [syn_scripts] file PanSyn provided
	-o	Full path to the folder storing the output results 
	-h|-help	Print this help page
		*************************************************\n";
}

open I,"<$opts{g1}" or die("Could not open $opts{g1}\n"); 
my %h1=();my %h1_name=();

while (my $a=<I>) {
	chomp $a;
	my @it=split/\t/,$a;
	if (!exists $h1{$it[0]}) {
		$h1{$it[0]}=1;
	}
	else{
		$h1{$it[0]}=$h1{$it[0]}+1;
	}
	$h1_name{$it[1]}=$it[0];
}
close I;


open I,"<$opts{g2}" or die("Could not open $opts{g2}\n"); 
my %h2=();my %h2_name=();

while (my $a=<I>) {
	chomp $a;
	my @it=split/\t/,$a;
	if (!exists $h2{$it[0]}) {
		$h2{$it[0]}=1;
	}
	else{
		$h2{$it[0]}=$h2{$it[0]}+1;
	}
	$h2_name{$it[1]}=$it[0];
}
close I;

############count######
my %h1_count=();
foreach my $i (keys %h1_name) {
	$name=$h1_name{$i};
	$h1_count{$i}=$h1{$name};
}

my %h2_count=();
foreach my $i (keys %h2_name) {
	$name=$h2_name{$i};
	$h2_count{$i}=$h2{$name};
}

###choose annotation#####
open I,"<$opts{a}" or die("Could not open $opts{a}\n"); 
open O1,">$opts{o}/node_young.emapper.annotations" or die("Could not open $opts{o}/node_young.emapper.annotations\n"); 
open O2,">$opts{o}/node_old.emapper.annotations" or die("Could not open $opts{o}/node_old.emapper.annotations\n"); 

while (my $a=<I>) {
	chomp $a;
	if ($a!~/^#/) {
		my @it=split/\t/,$a;
		if (exists $h1_name{$it[0]}) {
			print O1 "$a\n";
		}
		if (exists $h2_name{$it[0]}) {
			print O2 "$a\n";
		}
	}
}
close I;close O1;close O2;
############annotation###
@all = ("A", "B", "C","D", "E", "F", "G", "H","I", "J", "K", "L", "M","N", "O", "P", "Q", "S","T", "U", "V", "W","Y", "Z");

open I,"<$opts{o}/node_young.emapper.annotations" or die("Could not open $opts{o}/node_young.emapper.annotations\n"); 
my %count1=();

while (my $a=<I>) {
	chomp $a;
	if ($a!~/^#/) {
		my @it=split/\t/,$a;
		if ($it[6] !~/-/) {
			my @wo=split//,$it[6];
			$num=@wo;
			foreach my $i (@wo) {
				if (!exists $count1{$i}) {
					$chu=1/$h1_count{$it[0]};
					$count1{$i}=$chu/$num;
				}
				else{
					$chu=1/$h1_count{$it[0]};
					$count1{$i}=$count1{$i}+$chu/$num;
				}
			}
		}
	}
}
close I;

my %ha=();
open O,">$opts{o}/node_young_categories.counts" or die("Could not open $opts{o}/node_young_categories.counts\n"); 
foreach my $i (@all) {
	if (exists $count1{$i}) {
		print O "$i\t$count1{$i}\n";
		$ha{$i}=$count1{$i};
	}
	else{
		print O "$i\t0\n";
		$ha{$i}=0;
	}
}
close O;


##node2
open I,"<$opts{o}/node_old.emapper.annotations" or die("Could not open $opts{o}/node_old.emapper.annotations\n"); 
my %count2=();
#$na=0;
while (my $a=<I>) {
	chomp $a;
	if ($a!~/^#/) {
		my @it=split/\t/,$a;
		if ($it[6] !~/-/) {
			my @wo=split//,$it[6];
			$num=@wo;
			foreach my $i (@wo) {
				#if ($i eq "A") {
				#	print "$it[0]\n";
				#	$na=$na+1;
				#}
				if (!exists $count2{$i}) {
					$chu=1/$h2_count{$it[0]};
					$count2{$i}=$chu/$num;
				}
				else{
					$chu=1/$h2_count{$it[0]};
					$count2{$i}=$count2{$i}+$chu/$num;
				}
			}
		}
	}
}
close I;
my %hb=();
#print "$na\n";
open O,">$opts{o}/node_old_categories.counts" or die("Could not open $opts{o}/node_old_categories.counts\n"); 
foreach my $i (@all) {
	if (exists $count2{$i}) {
		print O "$i\t$count2{$i}\n";
		$hb{$i}=$count2{$i};
	}
	else{
		print O "$i\t0\n";
		$hb{$i}=0;
	}
}
close O;

open O,">$opts{o}/node_old-young_categories.counts" or die("Could not open $opts{o}/node_old-young_categories.counts\n"); 
print O "Categories\tValues\n";
foreach my $i (@all) {
	$cha=$ha{$i}-$hb{$i};
	print O "$i\t$cha\n";
}

system("Rscript $opts{s}/Categories.r $opts{o} $opts{o}/node_old-young_categories.counts");
